// ASSIGMENT
let val = 0;
val += 3; // val = val + 3
val -= 3; // val = val - 3
val *= 3; // val = val * 3
val /= 3; // val = val / 3

// COMPARISON
let x = 5;
x === 5 // true
x === '5' // false
x !== 5 // false
x !== 8 // true
x > 5 // false
x < 8 // true
x >= 5 // true
x >= 8 // false 
x <= 5 // true

// LOGICAL
// AND
(x < 10 && y > 1) // true
// OR
(x === 5 || y === 5) // false
// NOT
!(x == y) // true