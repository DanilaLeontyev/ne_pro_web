let animal = 'some';

switch (animal) {
    case 'dog':
        console.log('woof'); break;
    case 'cat':
        console.log('meow')
    case 'bird':
        console.log('tweet')
    case 'cow':
        console.log('moo')
    case 'fox':
        console.log('ring-ding-ding')
    default:
        console.log('enter the animal')
}